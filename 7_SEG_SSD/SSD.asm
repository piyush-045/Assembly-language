ORG 00H 
		P1M1 EQU 0B3H
		P1M2 EQU 0B4H
		MOV P1M1,#00H
		MOV P1M2,#0FFH
	
WHILE:	MOV R4,#10;    COUNTER
		MOV DPTR,#MYDATA  ;0R MOV DPTR,#0400H
	
BACK:	CLR A
		MOVC A,@A+DPTR  //MOVE DATA FROM ROM LOCATION 0400H - 16 Bit ADRESS 
		MOV P1,A
		ACALL DELAY
		INC DPTR
		DJNZ R4,BACK
		SJMP WHILE		
		
;DELAY SUBROUTINE
DELAY:  MOV R7,#100
D1:		MOV R6,#100
D2:		MOV R5,#255
D3:	    DJNZ R5,D3
		DJNZ R6,D2
		DJNZ R7,D1
		RET
		
ORG 0400H
MYDATA: DB 0C0H,0F9H,0A4H,0B0H,99H,92H,82H,0F8H,80H,90H //FOR CC COMAN CATHODE DISPLAY
	
END